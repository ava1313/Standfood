let heroCopyTicking = false;
let heroCopyTargetShift = 0;
let heroCopyCurrentShift = 0;
let heroCopyAnimationFrame = null;

function animateHeroCopyScrollFollow() {
  heroCopyCurrentShift += (heroCopyTargetShift - heroCopyCurrentShift) * 0.12;

  if (Math.abs(heroCopyTargetShift - heroCopyCurrentShift) < 0.2) {
    heroCopyCurrentShift = heroCopyTargetShift;
  }

  heroCopy.style.setProperty("--hero-copy-shift", `${heroCopyCurrentShift}px`);

  if (Math.abs(heroCopyTargetShift - heroCopyCurrentShift) >= 0.2) {
    heroCopyAnimationFrame = window.requestAnimationFrame(animateHeroCopyScrollFollow);
  } else {
    heroCopyAnimationFrame = null;
  }
}

function startHeroCopyAnimation() {
  if (heroCopyAnimationFrame === null) {
    heroCopyAnimationFrame = window.requestAnimationFrame(animateHeroCopyScrollFollow);
  }
}

function updateHeroCopyScrollFollow() {
  heroCopyTicking = false;

  if (!heroCopy || !heroCopyInner) {
    return;
  }

  if (window.innerWidth <= 1080) {
    heroCopyTargetShift = 0;
    startHeroCopyAnimation();
    return;
  }

  const cardRect = heroCopy.getBoundingClientRect();
  const innerHeight = heroCopyInner.offsetHeight;
  const safeBottomGap = 34;
  const availableSpace = Math.max(0, heroCopy.clientHeight - innerHeight - safeBottomGap);

  if (availableSpace <= 0) {
    heroCopyTargetShift = 0;
    startHeroCopyAnimation();
    return;
  }

  const triggerStart = 120;
  const triggerEnd = -(cardRect.height - innerHeight - 120);
  const progress = (triggerStart - cardRect.top) / (triggerStart - triggerEnd);
  const clampedProgress = Math.max(0, Math.min(1, progress));
  heroCopyTargetShift = availableSpace * clampedProgress;

  startHeroCopyAnimation();
}

function requestHeroCopyScrollFollow() {
  if (!heroCopyTicking) {
    heroCopyTicking = true;
    window.requestAnimationFrame(updateHeroCopyScrollFollow);
  }
}

updateHeroCopyScrollFollow();
window.addEventListener("scroll", requestHeroCopyScrollFollow, { passive: true });
window.addEventListener("resize", requestHeroCopyScrollFollow);

function createMapIframe() {
  if (!mapCard || mapCard.querySelector("iframe")) {
    return;
  }

  const currentLanguage = document.documentElement.lang in translations ? document.documentElement.lang : "el";
  const iframe = document.createElement("iframe");
  iframe.src = mapCard.dataset.mapSrc;
  iframe.width = "100%";
  iframe.height = "100%";
  iframe.style.border = "0";
  iframe.allowFullscreen = true;
  iframe.loading = "lazy";
  iframe.referrerPolicy = "no-referrer-when-downgrade";
  iframe.title = translations[currentLanguage].mapTitle;
  mapCard.appendChild(iframe);
  mapCard.classList.add("has-map");
}

function updateConsentUI(state) {
  if (!cookieBanner) {
    return;
  }

  if (state === "accepted") {
    cookieBanner.hidden = true;
    createMapIframe();
  } else if (state === "declined") {
    cookieBanner.hidden = true;
    if (mapCard) {
      mapCard.classList.remove("has-map");
      const iframe = mapCard.querySelector("iframe");
      if (iframe) {
        iframe.remove();
      }
    }
  } else {
    cookieBanner.hidden = false;
  }
}

function setConsent(state) {
  localStorage.setItem(consentStorageKey, state);
  updateConsentUI(state);
}

const savedConsent = localStorage.getItem(consentStorageKey);
updateConsentUI(savedConsent);

if (acceptCookiesButton) {
  acceptCookiesButton.addEventListener("click", () => setConsent("accepted"));
}

if (declineCookiesButton) {
  declineCookiesButton.addEventListener("click", () => setConsent("declined"));
}

if (acceptMapButton) {
  acceptMapButton.addEventListener("click", () => setConsent("accepted"));
}

if (cookieSettingsButton) {
  cookieSettingsButton.addEventListener("click", () => {
    if (cookieBanner) {
      cookieBanner.hidden = false;
      cookieBanner.scrollIntoView({ behavior: "smooth", block: "nearest" });
    }
  });
}

function applyTranslations(language) {
  const dictionary = translations[language] || translations.el;

  document.documentElement.lang = language;
  titleElement.textContent = dictionary.pageTitle;
  metaDescription.setAttribute("content", dictionary.pageDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (dictionary[key]) {
      element.alt = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const key = element.dataset.i18nTitle;
    if (dictionary[key]) {
      element.title = dictionary[key];
    }
  });

  if (mapCard) {
    const iframe = mapCard.querySelector("iframe");
    if (iframe) {
      iframe.title = dictionary.mapTitle;
    }
  }

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

const savedLanguage = localStorage.getItem("stanfood-language");
const initialLanguage = savedLanguage && translations[savedLanguage] ? savedLanguage : "el";
applyTranslations(initialLanguage);

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const language = button.dataset.lang;
    localStorage.setItem("stanfood-language", language);
    applyTranslations(language);
  });
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.14 });

document.querySelectorAll("[data-reveal]").forEach((el) => observer.observe(el));

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  link.addEventListener("click", function(e) {
    const id = this.getAttribute("href");
    const target = document.querySelector(id);

    if (target) {
      e.preventDefault();
      const headerOffset = 76;
      const targetTop = target.getBoundingClientRect().top + window.scrollY - headerOffset;
      window.scrollTo({ top: targetTop, behavior: "smooth" });
    }
  });
});
